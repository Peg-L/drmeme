      const controlBtn = this.$refs.controlBtn;
      const episode = this.$refs.episode;
      if (controlBtn.classList.contains('fa-circle-pause')) {
        episode.pause();
        controlBtn.classList.remove('fa-circle-pause');
        controlBtn.classList.add('fa-circle-play');
